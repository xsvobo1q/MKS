/*   ------ Debounce hendler for buttons ------*/
void button(bool *p_state1, bool *p_state2){

	static uint16_t debounceS1 = 0xFFFF;
	static uint16_t debounceS2 = 0xFFFF;
	static uint32_t debounceDelay = 0;

	if(HAL_GetTick() > debounceDelay + DEBOUNCE_CONST){

		debounceS1 <<= 1;
		debounceS2 <<= 1;

		debounceDelay = HAL_GetTick();

		if(!HAL_GPIO_ReadPin(S1_GPIO_Port,S1_Pin)) debounceS1 |= 0x0001;
		if(!HAL_GPIO_ReadPin(S2_GPIO_Port,S2_Pin)) debounceS2 |= 0x0001;

		if(debounceS1 == 0x7FFF) *p_state1 =true;
		if(debounceS2 == 0x7FFF) *p_state2 = true;

	}else{
		*p_state1 = false;
		*p_state2 = false;
	}

}

/*  ---- Reading of AD conversion ----*/
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc){

	static volatile uint32_t ADCavg;

	ADCval = ADCavg >> ADC_Q;
	ADCavg -= ADCval;
	ADCavg += HAL_ADC_GetValue(hadc);
}

